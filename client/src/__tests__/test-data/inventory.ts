// Generate a large dataset for performance testing
export const generateLargeInventory = (count: number): InventoryItem[] => {
  return Array.from({ length: count }, (_, i) => ({
    id: `item-${i + 1}`,
    companyId: "company-1",
    sku: `SKU-${1000 + i}`,
    name: `Product ${i + 1}`,
    description: `Description for product ${i + 1}`,
    category: i % 2 === 0 ? "Category A" : "Category B",
    unitCost: Math.floor(Math.random() * 900) + 100,
    unitPrice: Math.floor(Math.random() * 1000) + 200,
    quantityOnHand: Math.floor(Math.random() * 50),
    quantityReserved: Math.floor(Math.random() * 5),
    quantityAvailable: 0, // Will be calculated
    reorderPoint: 5,
    reorderQuantity: 10,
    maxStockLevel: 100,
    minStockLevel: 2,
    supplierId: `supplier-${(i % 3) + 1}`,
    isActive: i % 10 !== 0, // Make 10% of items inactive
    trackInventory: true,
    lastStockUpdate: new Date(
      Date.now() - Math.floor(Math.random() * 30) * 24 * 60 * 60 * 1000,
    ).toISOString(),
    barcode: `barcode-${100000000000 + i}`,
    weight: Math.random() * 5,
    dimensions: "N/A",
    location: `A${Math.floor(i / 100) + 1}-${(i % 10) + 1}`,
    notes: "",
    createdAt: new Date(
      Date.now() - Math.floor(Math.random() * 365) * 24 * 60 * 60 * 1000,
    ).toISOString(),
    updatedAt: new Date().toISOString(),
  })).map((item) => ({
    ...item,
    quantityAvailable: Math.max(0, item.quantityOnHand - item.quantityReserved),
  }));
};

